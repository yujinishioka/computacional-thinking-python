# entrada de dados
numA = int(input(" Digite um numero: "))
numB = int(input(" Digite outro numero: "))

soma = numA + numB
sub = numA - numB
prod = numA * numB
divr = numA / numB
pot = numA ** numB
resto = numA % numB
divi = numA // numB

print(" Soma: ", soma)
print(" Subtracao: ", sub)
print(" Produto: ", prod)
print(" Divisao real: ", divr)
print(" Potencia: ", pot)
print(" Resto: ", resto)
print(" Divisao inteira: ", divi)
